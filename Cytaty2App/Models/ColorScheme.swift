import Foundation
import SwiftUI

struct AppColorScheme: Identifiable, Codable {
    let name: String
    let displayName: String
    let background: String
    let primaryText: String
    let secondaryText: String
    let accent: String
    let uiElement: String
    let isDarkVariant: Bool
    
    // ID generowane na podstawie nazwy - będzie spójne
    var id: String {
        return name
    }
    
    // Konwersja hex na Color
    var backgroundColor: Color {
        Color(hex: background)
    }
    
    var primaryTextColor: Color {
        Color(hex: primaryText)
    }
    
    var secondaryTextColor: Color {
        Color(hex: secondaryText)
    }
    
    var accentColor: Color {
        Color(hex: accent)
    }
    
    var uiElementColor: Color {
        Color(hex: uiElement)
    }
    
    // Backward compatibility - obliczana właściwość
    var isDark: Bool {
        return isDarkVariant
    }
}

// Rozszerzenie Color do obsługi hex
extension Color {
    init(hex: String) {
        let hex = hex.trimmingCharacters(in: CharacterSet.alphanumerics.inverted)
        var int: UInt64 = 0
        Scanner(string: hex).scanHexInt64(&int)
        let a, r, g, b: UInt64
        switch hex.count {
        case 3: // RGB (12-bit)
            (a, r, g, b) = (255, (int >> 8) * 17, (int >> 4 & 0xF) * 17, (int & 0xF) * 17)
        case 6: // RGB (24-bit)
            (a, r, g, b) = (255, int >> 16, int >> 8 & 0xFF, int & 0xFF)
        case 8: // ARGB (32-bit)
            (a, r, g, b) = (int >> 24, int >> 16 & 0xFF, int >> 8 & 0xFF, int & 0xFF)
        default:
            (a, r, g, b) = (1, 1, 1, 0)
        }

        self.init(
            .sRGB,
            red: Double(r) / 255,
            green: Double(g) / 255,
            blue:  Double(b) / 255,
            opacity: Double(a) / 255
        )
    }
}

// Rozszerzenie UIColor do obsługi hex
